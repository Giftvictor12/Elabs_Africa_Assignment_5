// startsWith() returns a Boolean value—true or false—depending on 
// whether the string starts with the substring you have specified

var car_rankings = "Toyota,Lexus,Benz,volvo,Ford";

console.log(car_rankings.startsWith("Toyota"));


// endsWith() is the opposite of the startsWith() method. 
// The endsWith() method determines if a string ends with a particular set of characters.

var food_rankings = "rice,bread,garri,yam,noddles,beans";

console.log(food_rankings.endsWith("beans"));